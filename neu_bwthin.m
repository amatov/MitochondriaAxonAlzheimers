function y = neu_bwthin (x)
% y = bwthin (x)
%
% Perform thinning on a binary image by Guo's method. See Louisa Lam,
% Seong-Whan Lee, and Ching Y. Wuen, "Thinning Methodologies - A
% Comprehensive Survey," IEEE TPAMI, vol. 14, no. 9, pp. 869-885, 1992. The
% algorithm is described at the bottom of the first column and the top of
% the second column on page 879.
%
% In:
%  x    binary image
%
% Out:
%  y    skeleton of the input image
% 
% Copyright (C) 2004-2005 Antti Niemistö
% See angioquant.m for more copyright information.

lut1 = lutthin1;
lut2 = lutthin2;

y = x;
done = 0;

while ~done
  lasty = y;
  y = applylut(applylut(y,lut1),lut2);
  done = isequal(lasty,y);
end


